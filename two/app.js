"use strict";
// 基本数据类型
/* number类型 */
var num = 25;
var flo = 23.5;
var hex = 0xf00;
var binary = 9;
var octal = 475;
// 等同于
// let num: number = 25
/* boolean类型 */
var isLogin = false;
// 等同于
// let isLogin: boolean = false
/* string类型 */
var str = 'hello world';
/* any类型 */
var anything;
// 等同于
// let anything: any
anything = 25;
anything = 'hello';
