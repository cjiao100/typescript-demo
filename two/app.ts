// 基本数据类型

/* number类型 */
let num = 25;
let flo = 23.5;
let hex = 0xf00;
let binary = 0b1001;
let octal = 0o733;
// 等同于
// let num: number = 25

/* boolean类型 */
let isLogin = false;
// 等同于
// let isLogin: boolean = false

/* string类型 */
let str: string = 'hello world';

/* any类型 */
let anything;
// 等同于
// let anything: any
anything = 25;
anything = 'hello';
